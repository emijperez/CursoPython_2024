
# Diicionarios : El literal que vamos a utilizar es : '{}'

# Cada elemento del diccionario esta conformado por : calve:valor, clave:valor, etc

my_diccionario = {'1':'Darwin', '2':'Python', '3':'Java', '4':'C++'}
print(type(my_diccionario))
print(my_diccionario)

print(my_diccionario['1'])
print(my_diccionario[2])   # tener cuidado al definir las llaves