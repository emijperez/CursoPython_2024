
nota = float(input("Ingresa la nota final de pase de ano \n"))
    
if nota >= 9:
    print('Pasaste el ano con "A" ')
elif nota >= 8:
    print('Pasaste el ano con "B" ')
elif nota >= 7:
    print('Pasaste el ano con "C" ')
elif nota >= 6:
    print('Pasaste el ano con "D" ')
else:
    print("Has reprobado el ano")